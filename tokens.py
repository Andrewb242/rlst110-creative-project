class Tokenizer:
    def is_valid_token(self, token):
        is_valid = True
        if token in stopwords:
            is_valid = False
        return is_valid

    def tokenize(self, string: str) -> list[str]:
        """
        Tokenizes a sting, excluding invalid tokens
        """

        passover_tokens = [
            ",",
            ":",
            ";",
            ".",
            ",",
            "?",
            "!",
            "\u201c",
            "\u201d",
            "\u2019",
            "\u2018",
        ]
        separator_tokens = ["\u2014", " ", "\n"]
        tokens = []
        word = ""

        for char in string:
            if char in separator_tokens:
                if self.is_valid_token(word):
                    tokens.append(word)
                word = ""
            elif char in passover_tokens:
                continue
            else:
                word += char
        if self.is_valid_token(word):
            tokens.append(word)
        return tokens
